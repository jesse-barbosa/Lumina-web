<script>
    getDate()
    function getDate(){
        var agora = new Date()
        var hora = agora.getHours()
        var diaMes = agora.getDate();
        var diaSem = agora.getDay();
        // Array com os nomes dos dias da semana
        var diasDaSemana = ["Domingo", "Segunda-feira", "Terça-feira", "Quarta-feira", "Quinta-feira", "Sexta-feira", "Sábado"];
        var saudacao = document.getElementById('titulo-central')
        
        if (hora < 12) {
            saudacao.innerHTML = `Bom dia, hoje é dia ${diaMes} - ${diasDaSemana[diaSem]}.`
        }
        else if (hora < 18){
            saudacao.innerHTML = `Boa tarde, hoje é dia ${diaMes} - ${diasDaSemana[diaSem]}.`
        }
        else {
            saudacao.innerHTML = `Boa noite, hoje é dia ${diaMes} - ${diasDaSemana[diaSem]}.`
        }
    }
</script>
